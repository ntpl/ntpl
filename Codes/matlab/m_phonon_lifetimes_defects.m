imode
    g(1) =...
        (1-NMD.alloy_conc)* ((1 - (NMD.m(1)/NMD.vm) )^2) ... 
        +...
        (NMD.alloy_conc)* ((1 - (NMD.m(2)/NMD.vm) )^2);
%find all e*(k',v') dot e(k,v), sum over b
    SUMb =...
        g(1)*...
        abs(...
        sum(...
        bsxfun(...
        @times, eig(:,:)  ,  conj(eig(imode,:)) ),2) ...
        ).^2 ;
%set self-term 0
    SUMb(imode) = 0;
%
ALLOY.dw_avg =...
    real(...
    mean(...
    ALLOY.freq(2:NMD.NUM_MODES)...
    -...
    ALLOY.freq(1:NMD.NUM_MODES-1)));
