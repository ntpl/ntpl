    ALLOY.freq - ALLOY.freq(imode);
lor = (1.0/pi)*(ALLOY.dw_avg./( delwij.^2 + ALLOY.dw_avg^2 ) );

    ALLOY.life(imode) =...
        ( pi*(ALLOY.freq(imode)^2) ) / ( 2*NMD.Nx*NMD.Ny*NMD.Nz )*...
        sum(lor.*SUMb,1);
    
    ALLOY.life(imode) = 1/ALLOY.life(imode);
        
